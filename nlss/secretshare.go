	s0 := make([]int, len(ss.alpha1))
	for i := 0; i < len(ss.alpha1); i++ {
		temp := (tempMat[i] + ss.alpha1[i]) % 2
		s0[i] = temp
	}

	ss.y1 = ss.CheckComply(ss.y1, s0)
