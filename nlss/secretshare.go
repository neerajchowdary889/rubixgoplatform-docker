// MultMatrix ...
func (ss *SecretShare) MultMatrix(firstMatrix []int, secondMatrix [][]int, c1 int, c2 int) []int {
	product := make([]int, c2)
	for i := 0; i < c2; i++ {
		sum := 0
		for j := 0; j < c1; j++ {
			sum = sum + secondMatrix[j][i]*firstMatrix[j]
		}
		product[i] = sum
	}
	return product
}

// CheckComply ...
func (ss *SecretShare) CheckComply(cand []int, s0 []int) []int {
	var sum int
	for {
		sum = 0
		for i := 0; i < len(cand); i++ {
			sum = sum + (cand[i] * s0[i])
		}
		sum = sum % 2
		if sum != ss.s {
			cand = ss.GenArray(V8, 8)
			cand = ss.CheckComply(cand, s0)
		}
		if sum <= ss.s {
			break
		}
	}
	return cand
}

